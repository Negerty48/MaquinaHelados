    private void refreshTable() {
        try {
            DefaultTableModel m = (DefaultTableModel) this.jTable1.getModel();
            ArrayList<Helado> al = this.mh.getHelados();
            for (Helado h : al) {
                Object[] o = {h.getPosicion(), h.getNombre(), h.getPrecio(), h.getTipo(), h.getCantidad()};
                m.addRow(o);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public ArrayList<Venta> getVentas() throws Exception {
        ArrayList<Venta> al = new ArrayList<>();
        try {
            al = this.mh.getVentas();
        } catch (Exception e) {
            throw e;
        }
        return al;
    }
    
